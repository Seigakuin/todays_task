# Task4-3: 数字が同じかどうかを確認しよう！
# -----------------------------

answer = 9
userinput = input("数字を当ててください")
userinput = int(userinput)

# ２つの値を比較しよう
# 同じかどうかを比較するるのは == イコールを２つつなげたものを使うよ
# >> print(userinput == answer)
# [[↓自分で打ってみよう↓]]
print(userinput == answer)

# 等しい場合は何が出力されるかな？
# 等しくない場合は何が出力されるかな？
print(9 == answer)
# True と False が大事！
