# Task2: 変数を理解しよう!

# 変数 name に自分の名前を入れよう
# >> name = "あなたの名前"

name = "太郎"

# 名前を出力しよう（出力結果に注目！）
# >> print(name)
print(name)

# 変数 x と変数 y に数字を入れよう
# >> x = 好きな数字
# >> y = 好きな数字
x = 12
y = 34

# x と y を足した結果を出力仕様
# >> print(x + y)
print(x + y)

# 出力だけでなく、入力を受け取ろう
# 変数 userinput に好きな食べ物を入れよう
# >> userinput = input("好きな食べ物はなんですか？")
userinput = input("好きな食べ物はなんですか")

# 変数 userinput を出力しよう
# >> print(userinput)
print(userinput)

# 文字列 str をつなげよう
# userinput で受け取った文字列にことばを付け足そう
# print("あなたの好きな食べ物は" + userinput + "です。")
print("あなたの好きな食べ物は" + userinput + "です。")
